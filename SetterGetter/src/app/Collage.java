package app;// SETTER AND GETTER TASK: -

public class Collage{
	private String clgName;
	private int clgId;
	private String clgAddress;
	
	public void setClgName(String clgName){
		this.clgName = clgName;
	}
	public void setClgId(int clgId){
		this.clgId = clgId;
	}
	public void setClgAddress(String clgAddress){
		this.clgAddress = clgAddress;
	}
	
	public String getClgName(){return clgName;}
	public int getClgId(){return clgId;}
	public String getClgAddress(){return clgAddress;}

}