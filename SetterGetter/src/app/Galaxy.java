package app;
// SETTER AND GETTER TASK: -

public class Galaxy{
	private String gname;
	private double garea;
	private long gdistance;
	
	public void setGname(String gname){
		this.gname = gname;
	}
	public void setGarea(double garea){
		this.garea = garea;
	}
	public void setGdistance(long gdistance){
		this.gdistance = gdistance;
	}
	
	public String getGname(){return gname;}
	public double getGarea(){return garea;}
	public long getGdistance(){return gdistance;}
}

