package com;
public class Student{
	private int rollno;
	private String name;
	private char grade;
	
	public void setRollno(int rollno){
		this.rollno = rollno;
	}
	public int getRollno(){ 
		return rollno; 
	}
	public void setName(String name){
		this.name = name;
	}
	public String getName() {
		return name;
	}
	public void setGrade(char grade){
		this.grade = grade;
	}
	public char getGrade(){
		return grade;
	}
	

}