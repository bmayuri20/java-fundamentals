package com;
// Constructor

public class Laptop{
	 String brand;
	 String model;
	 double price;
	
	public Laptop(){
		brand = "DELL";
		model = "ZX34RT";
		price = 60000;
	}
	
	
	public Laptop(String brand, String model, double price){
		this.brand = brand;
		this.model = model;
		this.price = price;
	}
	

}